const { TYPE_MAP, DEFAULT_VALUES, toPascalCase, toClassName } = require('./types.cjs');

/**
 * Generate TypeScript class (.gen.ts) from a NodeDefinition.
 */
function generate(def, edges, nodeMap) {
  const ts = TYPE_MAP.typescript;
  const dv = DEFAULT_VALUES.typescript;
  const className = toClassName(def.type);
  const isOneShot = !def.pins?.outputs?.length;

  // ── Pins ──
  const pins = [];
  for (const input of (def.pins?.inputs || [])) {
    pins.push(`  static readonly In_${toPascalCase(input.id)} = '${input.id}';`);
  }
  for (const output of (def.pins?.outputs || [])) {
    pins.push(`  static readonly Out_${toPascalCase(output.id)} = '${output.id}';`);
  }

  // ── Params ──
  const fields = [];
  const ctors = [];
  for (const p of (def.params || [])) {
    const t = ts[p.type] || 'string';
    const prop = toPascalCase(p.key);
    const defVal = JSON.stringify(p.default);
    fields.push(`  readonly ${prop}: Param<${t}>;`);
    ctors.push(`    this.${prop} = new Param<${t}>(
      this._source('${p.key}'),
      this._value<${t}>('${p.key}', ${defVal}),
      this._paramKey('${p.key}'),
    );`);
  }

  // ── Outs ──
  const outFields = [];
  const setOutCases = [];
  for (const output of (def.pins?.outputs || [])) {
    const fieldName = 'out' + toPascalCase(output.id);
    outFields.push(`  ${fieldName}: INodeHandler | null = null;`);
    setOutCases.push(`      case '${output.id}': this.${fieldName} = target; break;`);
  }
  const hasOuts = setOutCases.length > 0;

  // ── Stubs ──
  const onTickBody = isOneShot
    ? '    return NodeStatus.Complete;'
    : '    return NodeStatus.Running; // Override in subclass';

  return `// ⚠️ AUTO-GENERATED by FlowForge CLI — DO NOT EDIT
// Source: ${def.type} (${def.label})
// Generated: ${new Date().toISOString()}

import { INodeHandler, IWireOuts, INodeDataReceiver, IRuntime, Context, Param, NodeStatus } from '../core';

export class ${className} implements INodeHandler, IWireOuts, INodeDataReceiver {
  // ── Pins ──
${pins.join('\n')}

  // ── Params ──
${fields.join('\n')}

  // ── Node data ──
  private _nodeData: Record<string, unknown> = {};
${hasOuts ? '\n  // ── Outs ──\n' + outFields.join('\n') + '\n' : ''}
  constructor() {
${ctors.join('\n')}
  }

  // ── Lifecycle ──
  onEnter(rt: IRuntime): void { }

  onTick(rt: IRuntime, delta: number): NodeStatus {
${onTickBody}
  }

  onExit(rt: IRuntime): void { }
${hasOuts ? `
  // ── IWireOuts ──
  setOut(pinId: string, target: INodeHandler): void {
    switch (pinId) {
${setOutCases.join('\n')}
    }
  }

  resolveOuts(_nodeMap: ReadonlyMap<string, INodeHandler>): void { }
` : `
  setOut(_pinId: string, _target: INodeHandler): void { }
  resolveOuts(_nodeMap: ReadonlyMap<string, INodeHandler>): void { }
`}
  // ── INodeDataReceiver ──
  receiveNodeData(json: Record<string, unknown>): void {
    this._nodeData = json;
  }

  // ── Helpers ──
  private _source(key: string): string {
    const ps = this._nodeData['params'] as Record<string, Record<string, unknown>> | undefined;
    return (ps?.[key]?.['source'] as string) ?? 'fixed';
  }

  private _value<T>(key: string, deflt: T): T {
    const ps = this._nodeData['params'] as Record<string, Record<string, unknown>> | undefined;
    return (ps?.[key]?.['value'] as T) ?? deflt;
  }

  private _paramKey(key: string): string | null {
    return this._source(key) === 'param'
      ? (this._nodeData['params'] as Record<string, Record<string, unknown>>)?.[key]?.['value'] as string ?? null
      : null;
  }
}
`;
}

function generateAll(defs, edges, outDir) {
  const fs = require('fs');
  const path = require('path');
  if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

  const generated = [];
  for (const def of defs) {
    const code = generate(def, edges);
    const fileName = toClassName(def.type) + '.gen.ts';
    fs.writeFileSync(path.join(outDir, fileName), code, 'utf-8');
    generated.push(fileName);
  }
  return generated;
}

module.exports = { generate, generateAll };
